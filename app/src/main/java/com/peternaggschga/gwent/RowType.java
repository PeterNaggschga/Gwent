package com.peternaggschga.gwent;

/**
 * An enum listing the different types of attack rows.
 */
public enum RowType {
    /**
     * Represents the row containing melee combat units.
     */
    MELEE,

    /**
     * Represents the row containing ranged combat units.
     */
    RANGE,

    /**
     * Represents the row containing siege combat units.
     */
    SIEGE
}
