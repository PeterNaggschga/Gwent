package com.peternaggschga.gwent.ui.settings;

/**
 * An {@link Enum} used to discern the different possible sections shown by {@link RuleActivity}.
 */
enum RuleSection {
    /**
     * Represents a section containing general background-information on the game Gwent.
     */
    GENERAL,

    /**
     * Represents a section containing information on the course of a game of Gwent.
     */
    COURSE,

    /**
     * Represents a section containing information on the factions playable in Gwent.
     */
    FACTIONS,

    /**
     * Represents a section containing information on the available commanders of the factions and their abilities.
     */
    COMMANDER,

    /**
     * Represents a section containing information on the different types of cards in Gwent.
     */
    CARDS,

    /**
     * Represents a section containing information on the possible abilities of cards in Gwent.
     */
    CARD_ABILITIES,

    /**
     * Represents a section containing information on special cards available in Gwent.
     */
    SPECIAL_CARDS
}
