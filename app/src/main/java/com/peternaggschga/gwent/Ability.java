package com.peternaggschga.gwent;

/**
 * An enum listing the different card abilities possible.
 */
public enum Ability {
    /**
     * Represents a card that has no ability.
     */
    NONE,

    /**
     * Represents a card that has the Commanders Horn ability, e.g. Dandelion.
     */
    HORN,

    /**
     * Represents a card that has the Summon Avenger ability.
     */
    REVENGE,

    /**
     * Represents a card that has the Tight Bond ability.
     */
    BINDING,

    /**
     * Represents a card that has the Morale Boost ability.
     */
    MORAL_BOOST
}
